double* expansion(matrix(*ff)(matrix, matrix, matrix), double x0, double d, double alpha, int Nmax, matrix ud1, matrix ud2)
{
	try
	{
		double* p = new double[2]{ 0,0 };
		solution S0(x0);
		solution S1(x0 + d);

		S0.fit_fun(ff, ud1,ud2);
		S1.fit_fun(ff, ud1, ud2);

		if (S0.y == S1.y) {
			p[0] = x0;
			p[1] = x0+d;
			return p;
		}
		if (S1.y > S0.y) {
			d *= -1;
			S1.x = S0.x + d;
			S1.fit_fun(ff, ud1, ud2);
			if (S1.y >= S0.y) {
				p[0] = x0+d;
				p[1] = x0 - d;
				return p;
			}
		}
		solution S2;
		int i = 1;
		do{
			S2.x = d * pow(alpha, i) + x0;
			S2.fit_fun(ff,ud1,ud2);

			if (solution::f_calls > Nmax)
				throw "exception";
			if (S2.y >= S1.y)
				break;
			S0 = S1;
			S1 = S2;
			++i;
		} while (true);

		if (d > 0) {
			p[0] = S0.x(0,0);
			p[1] = S2.x(0.0);
		}
		else {
			p[0] = S2.x(0,0);
			p[1] = S0.x(0,0);
		}

		return p;
	}
	catch (string ex_info)
	{
		throw ("double* expansion(...):\n" + ex_info);
	}
}

